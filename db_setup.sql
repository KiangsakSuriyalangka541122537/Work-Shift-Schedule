-- Script นี้จะเคลียร์ตารางเก่าและสร้างใหม่ใน Schema "public" 

-- 1. เคลียร์ตารางเดิม (Table-kik) ใน public เพื่อป้องกันปัญหา
DROP TABLE IF EXISTS public."Table-kik";

-- 2. สร้างตาราง Table-kik (ใน public)
CREATE TABLE public."Table-kik" (
  id text primary key,
  staff_id text not null,
  date text not null,
  shift_type text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. สร้างตาราง monthly_roster_status (ถ้ายังไม่มี)
CREATE TABLE IF NOT EXISTS public.monthly_roster_status (
    month_key text primary key, 
    is_published boolean default false,
    published_by text,
    updated_at timestamp with time zone default timezone('utc'::text, now())
);

-- 4. สร้างตาราง users (ถ้ายังไม่มี)
CREATE TABLE IF NOT EXISTS public.users (
  id bigint generated by default as identity primary key,
  username text not null unique,
  password text not null,
  name text not null,
  role text default 'user',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 5. ให้สิทธิ์การใช้งาน
GRANT ALL ON ALL TABLES IN SCHEMA public TO anon, authenticated, service_role;
GRANT ALL ON ALL SEQUENCES IN SCHEMA public TO anon, authenticated, service_role;

